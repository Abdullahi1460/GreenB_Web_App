export const getDeviceStats = () => {
  const totalBins = mockDevices.length;
  const onlineBins = mockDevices.filter(d => d.status === 'online').length;
  const fullBins = mockDevices.filter(d => d.isFull).length;
  const tamperAlerts = mockDevices.filter(d => d.tamperDetected).length;
  const averageFill = Math.round(
    mockDevices.reduce((acc, d) => acc + d.binPercentage, 0) / totalBins
  );

  return {
    totalBins,
    onlineBins,
    fullBins,
    tamperAlerts,
    averageFill,
  };
};
